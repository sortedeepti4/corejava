package loopingStatement;

public class WhileStatement {
	public static void main(String[] args) {
		int i = 1;
		while (i <= 5) {
			System.out.println(i);
			i++;
		}
		System.out.println("==========");
		int x = 5;
		while (x >= 0) {
			System.out.println(x);
			x--;

		}
	}

}
