
public class Singer {
	
	public void lata() {
		System.out.println("lata");
	}
	public void shreya() {
		System.out.println("shreya");
	}
	public void alka() {
		System.out.println("alka");
	}
	public void aasha() {
		System.out.println("aasha");
	}
	public void kavita() {
		System.out.println("kavita");
	}
	
	
	
	public static void main(String[] args) {
		System.out.println("Singer");
		System.out.println("------------------");
		Singer obj = new Singer();
		obj.lata();
		obj.shreya();
		obj.kavita();
		obj.alka();
		obj.aasha();
	}

}
